public class Smileys
{
  public static void main( String[] args )
  {
    laugh();
    wink();
    cry();
    hoot();
  }

  public static void laugh()
  {
    System.out.println( "(^u^)" ); // happy face
  }

  public static void wink()
  {
    System.out.println( "(>.^)" ); // happy wink
  }

  public static void cry()
  {
    System.out.println( "(T.T)" ); // eyes with tears
  }

  public static void hoot()
  {
    System.out.println( "(O.o)" ); // owl face
  }
}
